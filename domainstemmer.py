for suffix in suffixes:
    suffix = suffix.strip().lower().encode('idna')
    if suffix[0] == '!':
        suffix = suffix[1:]
        exceptions.add(suffix)
        continue
    if suffix[:2] == '*.':
        suffix = suffix[2:]
        wildcard.add(suffix)
        continue
    public.add(suffix)


def stem(domain):
    """
    Stem the provided domain.

    Return None if the domain is not under a public suffix.
    """
    domain = domain.strip().lower().encode('idna')
    labels = domain.split('.')
    length = len(labels)
    for index in range(length):
        domain = '.'.join(labels[index:])
        nindex = index + 1
        if nindex < length:
            suffix = '.'.join(labels[nindex:])
            if suffix in wildcard and \
                    domain not in exceptions:
                return domain
            if suffix in exceptions:
                return domain
            if suffix in public:
                return domain
    return None
